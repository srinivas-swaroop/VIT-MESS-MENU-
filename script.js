// Display current date and time
function updateDateTime() {
    const now = new Date();
    const dateTimeString = `${now.toLocaleDateString()} - ${now.toLocaleTimeString()}`;
    document.getElementById('date-time').textContent = dateTimeString;
}

// Set the menu items based on the time of the day
function updateMenu() {
    const currentHour = new Date().getHours();
    const date = new Date().getDate(); // Get the current day (e.g., 10, 11, etc.)

    // Find the correct day from mealPlanData by matching the date
    const dayMenu = MenuData.meal_plan.find(day => day.dates.includes(date.toString()));
    
    if (dayMenu) {
        // Depending on the time of the day, show the respective menu
        if (currentHour >= 6 && currentHour < 10) {
            document.getElementById('breakfast-menu').textContent = dayMenu.meals.breakfast.join(", ");
            document.getElementById('lunch-menu').textContent = 'Lunch will be available later.';
            document.getElementById('snacks-menu').textContent = 'Snacks will be available later.';
            document.getElementById('dinner-menu').textContent = 'Dinner will be available later.';
        } else if (currentHour >= 10 && currentHour < 14) {
            document.getElementById('breakfast-menu').textContent = 'Breakfast has ended.';
            document.getElementById('lunch-menu').textContent = dayMenu.meals.lunch.join(", ");
            document.getElementById('snacks-menu').textContent = 'Snacks will be available later.';
            document.getElementById('dinner-menu').textContent = 'Dinner will be available later.';
        } else if (currentHour >= 14 && currentHour < 17) {
            document.getElementById('breakfast-menu').textContent = 'Breakfast has ended.';
            document.getElementById('lunch-menu').textContent = 'Lunch has ended.';
            document.getElementById('snacks-menu').textContent = dayMenu.meals.snacks.join(", ");
            document.getElementById('dinner-menu').textContent = 'Dinner will be available later.';
        } else {
            document.getElementById('breakfast-menu').textContent = 'Breakfast has ended.';
            document.getElementById('lunch-menu').textContent = 'Lunch has ended.';
            document.getElementById('snacks-menu').textContent = 'Snacks have ended.';
            document.getElementById('dinner-menu').textContent = dayMenu.meals.dinner.join(", ");
        }
    } else {
        document.getElementById('breakfast-menu').textContent = 'No menu available for today.';
        document.getElementById('lunch-menu').textContent = 'No menu available for today.';
        document.getElementById('snacks-menu').textContent = 'No menu available for today.';
        document.getElementById('dinner-menu').textContent = 'No menu available for today.';
    }
}


  
// Update date, time, and menu
updateDateTime();
updateMenu();

// Refresh the date and time every second
setInterval(updateDateTime, 1000);
